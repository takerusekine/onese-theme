<div class="greeting">
<h3>ひとつのセンスで全てを幸せに…</h3>
<p>『ビジネスセンス』というように、いかなる商売でも試されるセンス力。</p>
<p>自分が扱う商品を、いかにセンスの良い『言葉のラッピング』で包み、商品に輝きを放たせお客様に興味をもっていただけるか、また、お客様の安心感を充たす為にはどういった言葉・口調・表情をするべきか、といった場面でもそのお客様にマッチする言動をセレクトする『センス』が必要になってくる。 </p>
<p>飲食の世界では当然、多数のお客様の味覚を魅了するために最も重要な味のセンス。</p>
<p>その『味のセンスが活きる』立地、店の雰囲気、スタッフのセレクト、サービス、メニューのデザイン、食器のセレクト等、ひとつひとつ全てにおいて『何が最適か』をセレクトする、こちらもまたセンス力が試される連続です。</p>
<p>弊社が手がける事業では、このたったひとつの特別なセンス(one sense＝onese)を用いて社会に貢献し、少しでも皆様の生活を快適に豊かに、そして幸せになっていただけるよう提案し続けたいと思っております。</p>
